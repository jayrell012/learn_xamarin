SELECT 
cdri.CONTROL_NUMBER
,cdri.ITEM_CODE
,cdri.ITEM_DESC
,cdrmu.USER_NAME as IT_INCHARGE
,cdr.REQUEST_DATE
,cdr.CATALOGED_TIMESTAMP
,(CASE cdr.STATUS WHEN 'ECD' THEN 'Executed' WHEN 'DBP' THEN 'Distribution Pending' END) as STATUS
FROM CDR_T_REQUEST_ITEM cdri
LEFT JOIN CDR_T_REQUEST cdr 
on cdri.CONTROL_NUMBER = cdr.CONTROL_NUMBER
LEFT JOIN CDR_M_USER cdrmu
on cdr.CATALOGED_ID = cdrmu.USER_ID
WHERE cdr.STATUS IN ('ECD','DBP') AND cdr.CONTROL_NUMBER = 'ADMN-1904000007'
ORDER BY (CASE cdr.STATUS WHEN 'ECD' THEN 'Executed' WHEN 'DBP' THEN 'Distribution Pending' END)
